const VISIBLE_COUNT = 10;
const ANCHOR = new Date("2026-03-05T00:00:00").getTime();
const MIN_GAP_MS = 2 * 60 * 1000;
const MAX_GAP_MS = 30 * 60 * 1000;

function seededRandom(seed: number): number {
  const x = Math.sin(seed * 127.1 + 311.7) * 43758.5453;
  return x - Math.floor(x);
}

function buildTimeline(): number[] {
  const gaps: number[] = [];
  for (let i = 0; i < feedActivities.length; i++) {
    const r = seededRandom(i + 1);
    gaps.push(MIN_GAP_MS + r * (MAX_GAP_MS - MIN_GAP_MS));
  }
  return gaps;
}

const GAPS = buildTimeline();
const TOTAL_CYCLE_MS = GAPS.reduce((sum, g) => sum + g, 0);

function getVisibleItems(now: number): { activity: string; ageMs: number }[] {
  const elapsed =
    (((now - ANCHOR) % TOTAL_CYCLE_MS) + TOTAL_CYCLE_MS) % TOTAL_CYCLE_MS;

  const eventTimes: { index: number; ageMs: number }[] = [];
  let cursor = 0;
  for (let i = 0; i < feedActivities.length; i++) {
    cursor += GAPS[i];
    if (cursor <= elapsed) {
      eventTimes.push({ index: i, ageMs: elapsed - cursor });
    }
  }

  eventTimes.sort((a, b) => a.ageMs - b.ageMs);

  return eventTimes.slice(0, VISIBLE_COUNT).map((e) => ({
    activity: feedActivities[e.index],
    ageMs: e.ageMs,
  }));
}

function formatAge(ms: number): string {
  const minutes = Math.floor(ms / 60000);
  if (minutes < 1) return "just now";
  if (minutes < 60) return `${minutes} min ago`;
  const hours = Math.floor(minutes / 60);
  if (hours < 24) return `${hours} hr ago`;
  return `${Math.floor(hours / 24)}d ago`;
}

